/**
 * area-data.ts
 *
 * 全国都道府県・市区町村データモジュール。
 * index.html の JS データをそのまま TypeScript モジュールとして移植。
 *
 * データソース:
 *   - 都道府県: 経済センサス-基礎調査 + 中小企業白書
 *   - 市区町村シェア: 人口比 + ビジネス集積補正
 *   - 市: 792件 (AM), 町: 743件 (AT), 村: 183件 (AV)
 */

// ---------------------------------------------------------------------------
// Type definitions
// ---------------------------------------------------------------------------

export interface PrefectureData {
  /** 事業所数 */
  est: number;
  /** 従業員数 */
  emp: number;
  /** 開業率 (%) */
  rate: number;
  /** 年間新設法人数 */
  nb: number;
  /** 業種別事業所数 */
  sectors: {
    service: number;
    retail: number;
    food: number;
    construct: number;
    manufacture: number;
  };
}

export interface CityData {
  /** 地域名 */
  n: string;
  /** 所属都道府県 */
  p: string;
  /** 都道府県内シェア (0〜1) */
  s: number;
}

export interface AreaLocation {
  /** 地域名 */
  name: string;
  /** 種別: 'pref' = 都道府県, 'city' = 市区町村 */
  type: 'pref' | 'city';
  /** 所属都道府県名 */
  pref: string;
  /** 都道府県内シェア */
  share: number;
  /** 表示ラベル (例: "東京都 渋谷区") */
  display?: string;
  /** 検索用文字列 */
  searchKey?: string;
  /** AM/AT/AV から自動補完された場合 true */
  estimated?: boolean;
}

export interface LocationResult {
  /** 表示名 */
  name: string;
  /** 事業所数 */
  est: number;
  /** 従業員数 */
  emp: number;
  /** 開業率 (%) */
  rate: number;
  /** 年間新設法人数 */
  nbYear: number;
  /** 月間新設法人数 */
  nbMonth: number;
  /** 月額費用目安 (円) */
  costMonth: number;
  /** 業種別事業所数 */
  sectors: {
    service: number;
    retail: number;
    food: number;
    construct: number;
    manufacture: number;
  };
}

