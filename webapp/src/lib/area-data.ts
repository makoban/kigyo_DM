// ---------------------------------------------------------------------------
// buildAllLocations: 検索候補リストを構築する関数
// index.html の allLocations 構築ロジックと同一
// ---------------------------------------------------------------------------

export function buildAllLocations(): AreaLocation[] {
  const allLocations: AreaLocation[] = [];

  // 都道府県を追加
  Object.keys(P).forEach((name) => {
    allLocations.push({ name, type: 'pref', pref: name, share: 1 });
  });

  // 逆引きマップ: 区名 → 親市名リスト
  const wardToCity: Record<string, string[]> = {};
  Object.entries(WARD_PARENTS).forEach(([city, wards]) => {
    wards.forEach((w) => {
      if (!wardToCity[w]) wardToCity[w] = [];
      wardToCity[w].push(city);
    });
  });

  // 市区町村を追加（区名は親市名を付与して重複を識別可能に）
  C.forEach((c) => {
    let label = c.n;
    if (c.n.endsWith('区') && wardToCity[c.n]) {
      const cities = wardToCity[c.n];
      // この都道府県内の親市を探す
      const parent = cities.find((city) => C.some((x) => x.n === city && x.p === c.p));
      if (parent) label = parent + c.n;
    }
    allLocations.push({
      name: c.n,
      type: 'city',
      pref: c.p,
      share: c.s,
      display: c.p + ' ' + label,
      searchKey: c.p + label + c.n,
    });
  });

  // AM/AT/AV から C に含まれない全市区町村を重み付き按分で自動補完
  const coveredShareByPref: Record<string, number> = {};
  Object.keys(P).forEach((p) => { coveredShareByPref[p] = 0; });
  C.forEach((c) => { coveredShareByPref[c.p] = (coveredShareByPref[c.p] || 0) + c.s; });

  const coveredSet = new Set(C.map((c) => c.p + '|' + c.n));

  // 各都道府県の未カバー自治体を重み付きで収集
  const uncByPref: Record<string, Array<{ name: string; w: number }>> = {};
  Object.keys(P).forEach((p) => { uncByPref[p] = []; });

  // 市(weight:3) → 町(weight:1) → 村(weight:0.5)
  const sources: Array<[Record<string, string>, number]> = [
    [AM, 3],
    [AT, 1],
    [AV, 0.5],
  ];
  sources.forEach(([src, w]) => {
    Object.entries(src).forEach(([pref, str]) => {
      str.split('|').forEach((name) => {
        if (!coveredSet.has(pref + '|' + name)) {
          uncByPref[pref].push({ name, w });
        }
      });
    });
  });

  Object.entries(uncByPref).forEach(([pref, items]) => {
    if (!items.length) return;
    const remaining = Math.max(0.01, 1 - (coveredShareByPref[pref] || 0));
    const totalW = items.reduce((s, i) => s + i.w, 0);
    items.forEach((item) => {
      const share = Math.max(0.001, Math.round((remaining * item.w / totalW) * 1000) / 1000);
      allLocations.push({
        name: item.name,
        type: 'city',
        pref,
        share,
        display: pref + ' ' + item.name,
        searchKey: pref + item.name,
        estimated: true,
      });
    });
  });

  return allLocations;
}

// ---------------------------------------------------------------------------
// calcData: 地域データを算出する関数
// index.html の calcData() と同一ロジック
// ---------------------------------------------------------------------------

export function calcData(loc: AreaLocation): LocationResult | null {
  const pd = P[loc.pref];
  if (!pd) return null;
  const s = loc.share;
  return {
    name: loc.type === 'pref' ? loc.name : (loc.display ?? loc.pref + ' ' + loc.name),
    est: Math.round(pd.est * s),
    emp: Math.round(pd.emp * s),
    rate: pd.rate,
    nbYear: Math.round(pd.nb * s),
    nbMonth: Math.round(pd.nb * s / 12),
    costMonth: Math.round(pd.nb * s / 12) * 380,
    sectors: {
      service: Math.round(pd.sectors.service * s),
      retail: Math.round(pd.sectors.retail * s),
      food: Math.round(pd.sectors.food * s),
      construct: Math.round(pd.sectors.construct * s),
      manufacture: Math.round(pd.sectors.manufacture * s),
    },
  };
}

// ---------------------------------------------------------------------------
// searchLocations: 地域をファジー検索する関数
// index.html の スコア付きフィルタロジックと同一
// ---------------------------------------------------------------------------

/**
 * クエリ文字列で地域を検索し、スコア順にソートして返す。
 *
 * @param allLocations - buildAllLocations() で構築した候補リスト
 * @param query        - 検索クエリ文字列
 * @param limit        - 返す最大件数（デフォルト: 12）
 * @returns スコア降順にソートされた AreaLocation の配列
 */
export function searchLocations(
  allLocations: AreaLocation[],
  query: string,
  limit = 12,
): AreaLocation[] {
  if (!query) return [];

  const scored: Array<{ loc: AreaLocation; score: number }> = [];

  allLocations.forEach((l) => {
    const name = l.name;
    const full = l.searchKey ?? l.pref + l.name;
    let score = 0;

    if (name === query) score = 100;                          // 完全一致
    else if (name.startsWith(query)) score = 80;             // 名前前方一致
    else if (l.pref === query || l.pref.startsWith(query)) score = 60; // 都道府県前方一致
    else if (name.includes(query)) score = 40;               // 名前部分一致
    else if (full.includes(query)) score = 20;               // 全文部分一致
    else return;

    scored.push({ loc: l, score });
  });

  // スコア降順でソート
  scored.sort((a, b) => b.score - a.score);

  return scored.slice(0, limit).map((s) => s.loc);
}
